int main(int argc, char **argv) 
{
	FILE *out = stdout;
	int have_lyrics, have_chords;
	struct ptbf *ret;
	debugging = 1;
	ret = ptb_read_file(argv[1], default_section_handlers);
	
	if(!ret) {
		perror("Read error: ");
		return -1;
	} 

	fprintf(stderr, "Successfully parsed '%s'\n", argv[1]);

	fprintf(out, "%% Generated by ptb2ly (C) 2004 Jelmer Vernooij <jelmer@samba.org>\n");
	fprintf(out, "%% See http://jelmer.vernstok.nl/oss/ptabtools/ for more info\n\n");
		
	fprintf(out, "\\version \"1.9.8\"\n\n");

	ly_write_header(out, ret);
	have_chords = ly_write_chords(out, ret);
	have_lyrics = ly_write_lyrics(out, ret);

	fprintf(out, "\\score{\n");
	fprintf(out, "       <<\n");
    if(have_lyrics) {
		fprintf(out, "    \\addlyrics\n"
    	  "		\\context Staff = one {\n"
       	  "		\\property Staff.autoBeaming = ##f\n"
      	  "		}\n"
		  " \\context Lyrics \\text\n");
	}

	if(have_chords) {
		fprintf(out, " \\context ChordNames \\acc\n");
	}


	fprintf(out, "   >>\n");

	fprintf(out, "\\paper { }\n");
	fprintf(out, "\\midi { }\n");
	fprintf(out, "}\n");
	
	return (ret?0:1);
}
